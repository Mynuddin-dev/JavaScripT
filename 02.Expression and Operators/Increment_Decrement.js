var x = 10
var y = 21

console.log(x)
console.log(x++) //first printed the main value and also evaluate but not show
console.log("After Post Increment:",x)

console.log("\n\n")
console.log(y)
console.log(y--)
console.log("After post Decrement:",y)

console.log("\n\n")

console.log(x)
console.log(++x) // first evaluate and print the value
console.log("After Pre Increment:",x)

console.log("\n\n")
console.log(y)
console.log(--y)
console.log("After Pre Decrement:",y)

